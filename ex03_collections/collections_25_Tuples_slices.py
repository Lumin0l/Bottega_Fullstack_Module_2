# You can slice tuples same as strings
# The syntax is nearly identical.

post = ('Python Basics', 'Intro guide to Python', 'Some cool python content', 'published')

print(post[:2]) # 'Python Basics' and 'Intro guide to...'

# All the same rules apply with tuples, so 3 elements: start, stop and interval:
print(post[1::2]) # Print from the first element on, to the end, every 2 elements.





