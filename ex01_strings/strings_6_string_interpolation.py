# Dynamic strings, code inside the strings

name = 'Kristine'
greeting = f'Hi {name}' # F as in printf, format ans stuff. In python it's with '{}'
print(greeting)

# It works with any code you want
sum = f'This is the sum 2+2: {2 + 2}'
print(sum)

# To actually show brackets you need to put double brackets
brackets = 'Show the brackets: {{here they are}}'
print(brackets)

